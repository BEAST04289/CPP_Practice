#include <iostream>
using namespace std;    
//RELATIONAL OPERATORS
// ==, !=, <, >, <=, >= (prints true or false)
 int main() {
    int a = 5, b = 10;
    
    cout << "a == b: " << (a == b) << endl; // false=0
    cout << "a != b: " << (a != b) << endl; // true=1
    cout << "a < b: " << (a < b) << endl;   // true
    cout << "a > b: " << (a > b) << endl;   // false
    cout << "a <= b: " << (a <= b) << endl; // true
    cout << "a >= b: " << (a >= b) << endl; // false
    
    return 0;}//gives answer in boolean form, 0 or 1